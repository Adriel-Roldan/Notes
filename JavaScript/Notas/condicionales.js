
//CONDICIONAL IF 

if (false){      // El IF se ejecuta siempre y cuando la condicion que tenga sea TRUE si es FALSE va a saltar al siguiente bloque que le sigue abajo.
    console.log("Si soy verdadero me ejecuto");
}
else if (true){      // El ELSE IF pide otra condicion para que se cumplan y siempre va por abajo del IF y se pueden poner cuantos ELSE IF querramos. 
    console.log("Si soy el IF es falso me analisan a mi a ver si soy verdadero");
}
else if (false){
    console.log("Si soy verdero me ejecuto yo");
}
else {      // El ELSE se utiliza cuando ninguna condicion anterior se cumplio y el ELSE no lleva ninguna condicion.
    console.log("Si nada de lo anterior se cumple entonces me ejecutan a mi");
}