// Si nosotros queremos agregar mas elementos dentro de nuestro componente React tiene una regla que es que si o si tienen que estar dentro de un elemento padre, para eso existen los 'fragmentos', que lo que hacen es actuar de un contenedor padre, pero sin afectar en el HTML. Seria de la siguiente manera: 


return(
    <>
        <h1>Hola Mundo!</h1>
        <p>Asi se usa un fragmento</p>
    </>
)